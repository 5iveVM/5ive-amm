use five_vm_mito::{stack::StackStorage, MitoVM, FIVE_VM_PROGRAM_ID};

fn main() {
    // Bytecode from multiple-functions.v
    let bytecode = vec![
        0x35, 0x49, 0x56, 0x45, 0x3a, 0x01, 0x03, // Header
        0x19, 0x05, 0x19, 0x03, 0x90, 0x02, 0x2a, 0x00, 0x03, // Start of code
        0x61, 0x64, 0x64, 0xd4, 0x19, 0x04, 0x19, 0x02, 0x90, 0x02, 0x30, 0x00, 0x08, 0x6d, 0x75,
        0x6c, 0x74, 0x69, 0x70, 0x6c, 0x79, 0xd5, 0xd0, 0xd1, 0x20, 0x07, 0xa5, 0x01, 0xa5, 0x02,
        0x20, 0x07, 0xa5, 0x01, 0xa5, 0x02, 0x22, 0x07, 0x00,
    ];

    let instruction_data = vec![0, 0]; // Call function 0, no params

    println!("Executing multiple-functions test...");
    let mut storage = StackStorage::new();
    let result = MitoVM::execute_direct(
        &bytecode,
        &instruction_data,
        &[],
        &FIVE_VM_PROGRAM_ID,
        &mut storage,
    );

    match result {
        Ok(value) => println!("Success! Result: {:?}", value),
        Err(e) => println!("Error: {:?}", e),
    }
}
