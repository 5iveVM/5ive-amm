//! Import Discovery Demo
//!
//! This demonstration shows how function names embedded in bytecode
//! can be used for ecosystem composability via `use ContractAddress::*`.

use five_dsl_compiler::import_discovery::{ImportDiscovery, MockContractStorage};
use five_protocol::opcodes;

fn main() {
    println!("Five DSL Import Discovery Demo");
    println!("=============================\n");

    // Create mock contract bytecode with embedded function names
    let contract_bytecode = create_mock_contract_bytecode();

    // Set up mock storage
    let mut storage = MockContractStorage::new();
    storage.add_contract("SwapContract123", contract_bytecode);

    // Discover functions from the contract
    match ImportDiscovery::discover_functions_from_bytecode(
        "SwapContract123",
        storage.get_bytecode("SwapContract123").unwrap(),
    ) {
        Ok(discovered) => {
            println!("✅ Successfully discovered interface from SwapContract123");
            println!(
                "📊 Total bytecode size: {} bytes",
                discovered.total_bytecode_size
            );
            println!("🔍 Found {} functions:\n", discovered.functions.len());

            // Print discovered functions
            for (name, func) in &discovered.functions {
                println!(
                    "  • {} (address: 0x{:04X}, {} parameters)",
                    name, func.address, func.param_count
                );
            }

            println!("\n📝 Generated Five DSL Interface:");
            println!(
                "{}",
                ImportDiscovery::generate_interface_declaration(&discovered)
            );

            // Demonstrate usage in import statements
            println!("🚀 Usage in Five DSL:");
            println!("use SwapContract123::*; // Imports all discovered functions");
            println!("use SwapContract123::{{swap_tokens, get_pool_info}}; // Selective import\n");

            // Show compiler integration
            let import_mapping = ImportDiscovery::create_import_mapping(&discovered, 1);
            println!("🔧 Compiler Integration (account_index=1):");
            for (name, (_contract, account_idx, func_offset)) in &import_mapping {
                println!(
                    "  {} -> CALL_EXTERNAL account[{}] offset(0x{:04X})",
                    name, account_idx, func_offset
                );
            }
        }
        Err(e) => {
            eprintln!("❌ Failed to discover functions: {}", e);
        }
    }
}

/// Create mock contract bytecode with embedded function names
fn create_mock_contract_bytecode() -> Vec<u8> {
    let mut bytecode = Vec::new();

    // Function 1: swap_tokens(token_a: Address, token_b: Address, amount: u64)
    bytecode.extend_from_slice(&[
        opcodes::CALL,
        3, // param_count
        0x00,
        0x01, // function_address (0x0100)
        11,   // name_len
        b's',
        b'w',
        b'a',
        b'p',
        b'_',
        b't',
        b'o',
        b'k',
        b'e',
        b'n',
        b's',
    ]);

    // Function 2: get_pool_info(pool_id: u64)
    bytecode.extend_from_slice(&[
        opcodes::CALL,
        1, // param_count
        0x00,
        0x02, // function_address (0x0200)
        13,   // name_len
        b'g',
        b'e',
        b't',
        b'_',
        b'p',
        b'o',
        b'o',
        b'l',
        b'_',
        b'i',
        b'n',
        b'f',
        b'o',
    ]);

    // Function 3: add_liquidity (repeated call - should use name reference)
    bytecode.extend_from_slice(&[
        opcodes::CALL,
        4, // param_count
        0x00,
        0x03, // function_address (0x0300)
        13,   // name_len
        b'a',
        b'd',
        b'd',
        b'_',
        b'l',
        b'i',
        b'q',
        b'u',
        b'i',
        b'd',
        b'i',
        b't',
        b'y',
    ]);

    // Function 4: Another call to swap_tokens (should use name reference in real compiler)
    bytecode.extend_from_slice(&[
        opcodes::CALL,
        3, // param_count
        0x00,
        0x04, // function_address (0x0400)
        0xFF, // name reference marker (would be generated by deduplication)
        0,    // index to first occurrence
    ]);

    // Add some other opcodes to make it realistic
    bytecode.extend_from_slice(&[
        opcodes::PUSH_U64,
        0x00,
        0x00,
        0x00,
        0x00,
        0x00,
        0x00,
        0x00,
        0x01, // value: 1
        opcodes::RETURN,
    ]);

    bytecode
}
