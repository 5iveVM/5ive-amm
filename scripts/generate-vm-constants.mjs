#!/usr/bin/env node

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import web3 from '../five-cli/node_modules/@solana/web3.js/lib/index.cjs.js';
import { loadClusterConfig, deriveVmAddresses } from './lib/vm-cluster-config.mjs';

const { PublicKey } = web3;
const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const MAX_SHARDS = 8;

function parseArgs(argv) {
  const args = {};
  for (let i = 2; i < argv.length; i++) {
    const a = argv[i];
    if (a === '--cluster' && argv[i + 1]) args.cluster = argv[++i];
    else if (a === '--config' && argv[i + 1]) args.config = argv[++i];
    else if (a === '--out' && argv[i + 1]) args.out = argv[++i];
  }
  return args;
}

function fmtU8Array32(bytes) {
  const rows = [];
  for (let i = 0; i < bytes.length; i += 8) {
    const row = bytes
      .slice(i, i + 8)
      .map((b) => `0x${b.toString(16).padStart(2, '0')}`)
      .join(', ');
    rows.push(`    ${row},`);
  }
  return rows.join('\n');
}

function buildRust({ cluster, programId, shardCount, vmStatePda, vmStateBump, feeVaultPdas }) {
  const programPk = new PublicKey(programId);
  const vmPk = new PublicKey(vmStatePda);
  const header = [
    '// AUTO-GENERATED FILE. DO NOT EDIT MANUALLY.',
    '// Generated by: scripts/generate-vm-constants.mjs',
    `// Cluster: ${cluster}`,
    `// Program ID: ${programId}`,
    `// Generated at: ${new Date().toISOString()}`,
    '',
    'use pinocchio::pubkey::Pubkey;',
    '',
    `pub const GENERATED_CLUSTER: &str = "${cluster}";`,
    `pub const VM_PROGRAM_ID: &str = "${programId}";`,
    'pub const VM_PROGRAM_ID_BYTES: [u8; 32] = [',
    fmtU8Array32(Array.from(programPk.toBytes())),
    '];',
    '',
    'pub const HARDCODED_VM_STATE_PDA: [u8; 32] = [',
    fmtU8Array32(Array.from(vmPk.toBytes())),
    '];',
    `pub const HARDCODED_VM_STATE_BUMP: u8 = ${vmStateBump};`,
    `pub const HARDCODED_FEE_VAULT_COUNT: u8 = ${shardCount};`,
    '',
  ];

  for (const v of feeVaultPdas) {
    const vaultPk = new PublicKey(v.address);
    header.push(`pub const HARDCODED_FEE_VAULT_${v.shardIndex}: [u8; 32] = [`);
    header.push(fmtU8Array32(Array.from(vaultPk.toBytes())));
    header.push('];');
    header.push(`pub const HARDCODED_FEE_VAULT_${v.shardIndex}_BUMP: u8 = ${v.bump};`);
    header.push('');
  }

  header.push('pub fn get_hardcoded_fee_vault(shard_index: u8) -> Option<Pubkey> {');
  header.push('    match shard_index {');
  for (const v of feeVaultPdas) {
    header.push(`        ${v.shardIndex} => Some(Pubkey::from(HARDCODED_FEE_VAULT_${v.shardIndex})),`);
  }
  header.push('        _ => None,');
  header.push('    }');
  header.push('}');
  header.push('');

  header.push('pub fn get_hardcoded_fee_vault_bump(shard_index: u8) -> Option<u8> {');
  header.push('    match shard_index {');
  for (const v of feeVaultPdas) {
    header.push(`        ${v.shardIndex} => Some(HARDCODED_FEE_VAULT_${v.shardIndex}_BUMP),`);
  }
  header.push('        _ => None,');
  header.push('    }');
  header.push('}');
  header.push('');

  return `${header.join('\n')}\n`;
}

function main() {
  const args = parseArgs(process.argv);
  if (!args.cluster || !['localnet', 'devnet', 'mainnet'].includes(args.cluster)) {
    throw new Error('Missing/invalid --cluster (localnet|devnet|mainnet)');
  }

  const configPath = path.resolve(args.config || path.join(__dirname, '..', 'five-solana', 'constants.vm.toml'));
  const outPath = path.resolve(args.out || path.join(__dirname, '..', 'five-solana', 'src', 'generated_constants.rs'));

  const profile = loadClusterConfig({ cluster: args.cluster, configPath });
  if (profile.feeVaultShardCount < 1 || profile.feeVaultShardCount > MAX_SHARDS) {
    throw new Error(`fee_vault_shard_count must be between 1 and ${MAX_SHARDS}`);
  }

  const derived = deriveVmAddresses(profile);
  const rust = buildRust({
    cluster: profile.cluster,
    programId: profile.programId,
    shardCount: profile.feeVaultShardCount,
    vmStatePda: derived.vmStatePda,
    vmStateBump: derived.vmStateBump,
    feeVaultPdas: derived.feeVaultPdas,
  });

  fs.writeFileSync(outPath, rust);
  console.log(`Generated constants: ${outPath}`);
  console.log(`  cluster: ${profile.cluster}`);
  console.log(`  program_id: ${profile.programId}`);
  console.log(`  vm_state: ${derived.vmStatePda} (bump=${derived.vmStateBump})`);
  for (const v of derived.feeVaultPdas) {
    console.log(`  fee_vault[${v.shardIndex}]: ${v.address} (bump=${v.bump})`);
  }
}

try {
  main();
} catch (e) {
  console.error(`Error: ${e.message}`);
  process.exit(1);
}
